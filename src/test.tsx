// Fonction utilitaire pour fusionner profond√©ment deux objets
function deepMerge(target: any, source: any): any {
  const output = { ...target };

  if (isObject(target) && isObject(source)) {
    Object.keys(source).forEach((key) => {
      if (isObject(source[key])) {
        if (!(key in target)) {
          output[key] = source[key];
        } else {
          output[key] = deepMerge(target[key], source[key]);
        }
      } else {
        output[key] = source[key];
      }
    });
  }

  return output;
}

function isObject(item: any): boolean {
  return item && typeof item === "object" && !Array.isArray(item);
}

export function getTranslation(lang: Language) {
  // Si fran√ßais, retourner directement
  if (lang === "fr") return translations.fr;

  // Sinon, fusionner avec le fran√ßais comme base
  const langTranslations = translations[lang];
  if (!langTranslations) return translations.fr;

  // Merge profond : fran√ßais en base, langue s√©lectionn√©e par-dessus
  return deepMerge(translations.fr, langTranslations);
}

